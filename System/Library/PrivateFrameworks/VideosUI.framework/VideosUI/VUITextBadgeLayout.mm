@interface VUITextBadgeLayout
- (UIEdgeInsets)padding;
@end

@implementation VUITextBadgeLayout

- (UIEdgeInsets)padding
{
  top = self->_padding.top;
  left = self->_padding.left;
  bottom = self->_padding.bottom;
  right = self->_padding.right;
  result.right = right;
  result.bottom = bottom;
  result.left = left;
  result.top = top;
  return result;
}

@end