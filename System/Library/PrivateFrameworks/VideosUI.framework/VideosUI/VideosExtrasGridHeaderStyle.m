@interface VideosExtrasGridHeaderStyle
- (UIEdgeInsets)insets;
@end

@implementation VideosExtrasGridHeaderStyle

- (UIEdgeInsets)insets
{
  top = self->_insets.top;
  left = self->_insets.left;
  bottom = self->_insets.bottom;
  right = self->_insets.right;
  result.right = right;
  result.bottom = bottom;
  result.left = left;
  result.top = top;
  return result;
}

@end