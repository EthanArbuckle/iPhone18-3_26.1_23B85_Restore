@interface NRPBSelectorItem
- (SEL)selector;
- (void)setSelector:(SEL)selector;
@end

@implementation NRPBSelectorItem

- (SEL)selector
{
  if (self->_selector)
  {
    return self->_selector;
  }

  else
  {
    return 0;
  }
}

- (void)setSelector:(SEL)selector
{
  if (selector)
  {
    selectorCopy = selector;
  }

  else
  {
    selectorCopy = 0;
  }

  self->_selector = selectorCopy;
}

@end